package jumpstatement;

public class Break_example1 {

	
		//print number from 1 to 5 but the range I got is from 1 to 20
	
	public static void main(String args[])
	{
		for(int s=1 ; s<=20; s++)
		{
			System.out.println(s);
			if ( s==5) // 5=5
				break;
			
			}
		System.out.println("loop started");
		
	}
}

