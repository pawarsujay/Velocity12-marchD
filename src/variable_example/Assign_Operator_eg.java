package variable_example;

public class Assign_Operator_eg {
public static void main(String args[])
{
	int a = 20;
	int sujay;
	sujay = a;
	System.out.println(sujay);
	
	// += (value in sujay :20)
	
	sujay+=a; // sujay 40 = sujay(20)+a(20)
	System.out.println(" += " + sujay); //40
	
	// *=
	sujay*=a; //sujay=sujay*a
	System.out.println("+: " + sujay);
	
	// /=
	sujay/=a; //sujay=sujay/a
	System.out.println(" +: " + sujay);
	
	//-=
	int b = 10;
	int rameshwar = b;
	System.out.println(" - = " + rameshwar);
	
	//%=
	rameshwar%=b;
	System.out.println(" % = " + rameshwar);
	
	
	
	
	
	
	
	
	
	
}
}
